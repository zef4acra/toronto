q=[.5,.5,.5,.5]
s=20
t=1/s
p=[.25,.25,.25,.25]
j=[0.0,0.0,0.0,0.0]
for i1 in 1:s-3
    for i2 in 1:s-2-i1
        for i3 in 1:s-1-i1-i2
            p[1]=i1*t
            p[2]=i2*t
            p[3]=i3*t
            p[4]=1-p[1]-p[2]-p[3]
            j[1]=(q[1]-1)^2+q[2]^2+q[3]^2+q[4]^2-((p[1]-1)^2+p[2]^2+p[3]^2+p[4]^2)
            j[2]=q[1]^2+(q[2]-1)^2+q[3]^2+q[4]^2-(p[1]^2+(p[2]-1)^2+p[3]^2+p[4]^2)
            j[3]=q[1]^2+q[2]^2+(q[3]-1)^2+q[4]^2-(p[1]^2+p[2]^2+(p[3]-1)^2+p[4]^2)
            j[4]=q[1]^2+q[2]^2+q[3]^2+(q[4]-1)^2-(p[1]^2+p[2]^2+p[3]^2+(p[4]-1)^2)
            jr=[(1/100)*round(100*j[1]),(1/100)*round(100*j[2]),(1/100)*round(100*j[3]),(1/100)*round(100*j[4])]
            sw="no"
            if (j[1]>0) && (j[2]>0) && (j[3]>0) && (j[4]>0)
                sw="yes"
            end
            println(sw," ",string(p)," ",string(jr))
        end
    end
end
